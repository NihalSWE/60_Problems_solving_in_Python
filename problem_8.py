# Write a Python program that calculates the absolute value of an integer entered by the user.

num=int(input("Enter a number : "))
if num > 0 :
    print(f"the absolute value of {num} is {abs(num)}")
elif -num>0:
    print(f"The absolute value of {num} is {abs(-num)}")
else:
    print("The input number is zero.")

